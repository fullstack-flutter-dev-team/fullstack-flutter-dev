--▣ FUNCTION 정의(생성)
-- 함수명 : FN_GENDER()
--                   ↑ SSN(주민등록번호)  ▶ 'YYMMDD-NNNNNNN'
-- '여자' OR '남자' OR '성별확인불가'

CREATE OR REPLACE FUNCTION FN_GENDER
(
   VSSN VARCHAR2
)
RETURN VARCHAR2
IS
   -- 주요 변수 선언 및 초기화
   VRESULT VARCHAR2(20);
BEGIN
   -- 연산 및 처리
   IF (SUBSTR(VSSN, 8, 1) IN ('1', '3'))
      THEN VRESULT := '남자';
   ELSIF (SUBSTR(VSSN, 8, 1) IN ('2', '4'))
      THEN VRESULT := '여자';
   ELSE 
      VRESULT := '성별확인 불가..';
   END IF;

   -- 최종 결과값 반환
   RETURN VRESULT;

END;
-- ==>> Function FN_GENDER이(가) 컴파일되었습니다.

