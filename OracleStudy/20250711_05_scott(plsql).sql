--▣ TBL_INSA 테이블의 급여 계산 전용 함수를 정의한다.
--   급여는 『(기본급 * 12) + 수당』 기반으로 연산을 수행한다.
--   함수명 : FN_PAY(기본급, 수당)
-----------------[ 실습 - START ] ----------------------------
CREATE OR REPLACE FUNCTION FN_PAY
(
   BASICPAY NUMBER
 , SUDANG NUMBER 
)
RETURN NUMBER
IS
    VRESULT NUMBER := 0;
BEGIN
   VRESULT := BASICPAY*12 + SUDANG;
   RETURN VRESULT;
END;
