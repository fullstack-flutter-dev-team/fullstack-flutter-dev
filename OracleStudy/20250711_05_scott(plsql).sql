--▣ TBL_INSA 테이블의 입사일을 기준으로
--   현재까지의 근무년수를 반환하는 함수를 정의한다.
--   단, 근무년수는 소숫점 이하 한자리까지 계산한 결과를 반환할 수 있도록 한다.
--   함수명 : FN_WORKYEAR(입사일)
-----------------[ 실습 - START ] ----------------------------
CREATE OR REPLACE FUNCTION FN_WORKYEAR
(
   IBSADATE DATE
)
RETURN NUMBER
IS
   VRESULT NUMBER := 0;
BEGIN
   -- VRESULT := EXTRACT(YEAR FROM SYSDATE) - EXTRACT(YEAR FROM IBSADATE);
   VRESULT := TRUNC(MONTHS_BETWEEN(SYSDATE,IBSADATE)/12);
   RETURN VRESULT;
END;
