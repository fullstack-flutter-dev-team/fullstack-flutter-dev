--▣ 임의의 정수 두 개를 매개변수(입력 파라미터)로 넘겨받아
--   A의 B승의 값을 반환하는 사용자 정의 함수를 작성한다,
-- 함수명 : FN_POW()
/* 
    사용 예)
        SELECT FN_POW(10, 3) "결과확인"
        FROM DUAL;
        ==>> 1000
 */
-----------------[ 강사님 풀이 - START ] ------------------------------

CREATE OR REPLACE FUNCTION FN_POW
(
   A NUMBER  -- 10
,  B NUMBER  -- 3
)
RETURN NUMBER
IS
   --VRESULT NUMBER := 0;
   VRESTULT NUMBER := 1;    -- 반복뭉을 활용한 누적곱 연산
   VNUM     NUMBER;
BEGIN
   FOR VNUM IN 1..B LOOP  -- VNUM에 1 ~ 3
      VRESULT := VRESULT * A; -- 1 * 10 * 10 * 10
   END LOOP;

   RETURN VRESULT;
END;
-- ==>> Function FN_POW이(가) 컴파일되었습니다.

-----------------[ 강사님 풀이 - END ] --------------------------------

-----------------[ 실습 - START ] ----------------------------
CREATE OR REPLACE FUNCTION FN_POW
(
    NUM1 IN NUMBER
   ,NUM2 IN NUMBER
)
RETURN NUMBER
IS
    NUMRESULT NUMBER;
BEGIN
   FOR x IN 1..NUM2 LOOP
     NUMRESULT := NUM1 * NUM1;
   END LOOP;

   -- 결과 값 반환
   RETURN NUMRESULT;
END;

