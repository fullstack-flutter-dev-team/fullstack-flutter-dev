		// 주요 변수 선언 및 초기화
		Scanner sc = new Scanner(System.in);
		int dan;			//-- 사용자로부터 입력받은 값을 저수 형태로 담아낼 변수(단)
		
		// 연산 및 처리
		do
		{
			System.out.print("원하는 단(구구단) 입력 : ");
			dan = sc.nextInt();
		}
		while (dan < 1  || dan > 9);	//-- 입력값이 조건에 맞게 처리되었는지에 대한 확인
		
		int n = 0;
		
		
		// 결과 출력(반복 출력)
		while (n < 9)
		{
			n++;
			System.out.printf("%d * %d = %d\n", dan, n, (dan*n));
		}
		
		// --결과 출력
		// 원하는 단(구구단) 입력 : 2
		// 2 * 1 = 2
		// 2 * 2 = 4
