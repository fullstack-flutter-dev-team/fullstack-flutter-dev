		// 원하는 단(구구단) 입력 : -5
		// 원하는 단(구구단) 입력 : 0
		// 원하는 단(구구단) 입력 : 2
		// 2 * 1 = 2
		// 2 * 2 = 4
		// 2 * 3 = 6
		// 2 * 4 = 8
